a, b= map(int, input(). split())
def plus(a,b):
    return a+b
print(plus(a,b))

def minus(a,b):
    return a-b
print(minus(a,b))

def multiplication(a,b):
    return(a*b)
print(multiplication(a,b))

def share(a,b):
    return(a//b)
print(share(a,b))

def rest(a,b):
    return(a%b)
print(rest(a,b))
